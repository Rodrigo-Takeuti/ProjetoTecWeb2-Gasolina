                <h2>FAQ - Perguntas frequentes</h2><br>
                
 <h3>Os preços são atuais? Qual a periodicidade de atualização?</h3>
Sim, os preços são atualizados semanalmente. O sistema utiliza dados coletados pela Agência Nacional do Petróleo, 
Gás Natural e Biocombustíveis (ANP). A ANP coleta os dados durante a semana. 
No final de semana, nosso sistema obtém os dados, associa com a localização geográfica dos postos e 
imediatamente disponibiliza através do website.
<hr></hr>

<h3>Não aparecem postos na minha cidade. Qual o motivo?</h3>
A Agência Nacional do Petróleo, Gás Natural e Biocombustíveis (ANP) coleta apenas uma amostra dos preços dos postos do Brasil,
focando principalmente nas cidades mais populosas e regiões distintas. Além disso, nosso sistema remove postos com dados desatualizados. 
Isso restringe o número de postos, porém garante a veracidade da informação apresentada.
<hr></hr>

<h3>Porque a navegação por cidade exibe mais postos do que o mapa?</h3>
No mapa exibimos apenas os postos os quais temos a localização geográfica precisa. 
Por hora, cerca de 20% dos postos não são apresentados no mapa devido a esta falta de precisão.
Em breve, estaremos trabalhando para exibir todos os postos disponíveis no mapa interativo.
<hr></hr>

<h3>Como faço para ordenar por preço do álcool ou diesel?</h3>
Por padrão, a lista de postos aparece ordenada pelo preço da gasolina, do mais barato ao mais caro.
Para ordenar por álcool, diesel ou pelo nome do posto, basta clicar no cabeçalho da tabela.
<hr></hr>

